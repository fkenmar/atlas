#!/usr/bin/env bash
# repomap agent-task benchmark runner (M0: real headless execution).
#
# Protocol: benchmark/README.md. For each task YAML, runs N headless Claude
# Code sessions per arm in a fresh checkout of the task's pinned repo,
# records tokens/turns/success, and reports medians. Results go to
# results/run-<stamp>.local.json (gitignored); the recorded no-map baseline
# in baseline.json is written ONLY via --record-baseline.
#
# Arms:
#   without_map  plain session (the baseline arm)
#   with_map     repomap output injected as appended system prompt — OFF by
#                default until budgeting lands (M1); the naive full map is
#                too large to inject fairly. Enable via BENCH_ARMS.
#
# Usage (task paths are resolved relative to benchmark/, wherever you call from):
#   ./benchmark/run.sh                      # run every task
#   ./benchmark/run.sh tasks/01-*.yaml      # run a subset (glob expands only if cwd is benchmark/)
#   ./benchmark/run.sh --record-baseline    # run without_map arm and write baseline.json
#
# Env knobs:
#   BENCH_MODEL      model for the agent sessions  (default: claude-sonnet-4-6)
#   BENCH_RUNS       runs per arm, protocol min 3  (default: 3)
#   BENCH_MAX_TURNS  per-session turn cap          (default: 30)
#   BENCH_ARMS       space-separated arm list      (default: without_map)
#   REPOMAP_BIN      repomap binary for with_map   (default: ../target/release/repomap)
#
# Requires: claude CLI (logged in), git, jq, python3 with PyYAML.
set -euo pipefail

cd "$(dirname "$0")"
mkdir -p results .work

BENCH_MODEL="${BENCH_MODEL:-claude-sonnet-4-6}"
BENCH_RUNS="${BENCH_RUNS:-3}"
BENCH_MAX_TURNS="${BENCH_MAX_TURNS:-30}"
BENCH_ARMS="${BENCH_ARMS:-without_map}"
REPOMAP_BIN="${REPOMAP_BIN:-../target/release/repomap}"

for tool in claude git jq python3; do
  command -v "$tool" >/dev/null 2>&1 || { echo "error: $tool is required" >&2; exit 1; }
done
python3 -c "import yaml" 2>/dev/null || { echo "error: python3 needs PyYAML (pip install pyyaml)" >&2; exit 1; }

record_baseline=false
tasks=()
for arg in "$@"; do
  case "$arg" in
    --record-baseline) record_baseline=true ;;
    *) tasks+=("$arg") ;;
  esac
done
if [ ${#tasks[@]} -eq 0 ]; then
  tasks=(tasks/*.yaml)
fi
for t in "${tasks[@]}"; do
  if [ ! -e "$t" ]; then
    echo "error: no such task file: $t (paths are relative to benchmark/)" >&2
    exit 1
  fi
done
if [ "$record_baseline" = true ]; then
  BENCH_ARMS="without_map"
fi

yaml_get() { # yaml_get <file> <dotted.key>
  python3 - "$1" "$2" <<'PYEOF'
import sys, yaml
doc = yaml.safe_load(open(sys.argv[1]))
node = doc
for part in sys.argv[2].split('.'):
    node = node[part]
print(node if not isinstance(node, str) else node.strip())
PYEOF
}

median_of() { # median_of <newline-separated numbers on stdin>
  sort -n | awk '{a[NR]=$1} END {if (NR==0) {print "null"; exit}; m=int((NR+1)/2); print a[m]}'
}

stamp="$(date +%Y%m%d-%H%M%S)"
out="results/run-${stamp}.local.json"
echo "repomap benchmark — ${#tasks[@]} task(s), arms: ${BENCH_ARMS}, ${BENCH_RUNS} run(s)/arm, model ${BENCH_MODEL}"

task_objects=()
for t in "${tasks[@]}"; do
  id="$(yaml_get "$t" id)"
  prompt="$(yaml_get "$t" prompt)"
  repo_url="$(yaml_get "$t" repo.url)"
  repo_rev="$(yaml_get "$t" repo.rev)"
  success_cmd="$(yaml_get "$t" success.cmd)"

  # Pinned-rev cache clone, shared by every run of this task.
  cache=".work/cache/${id}-${repo_rev}"
  if [ ! -d "$cache/.git" ]; then
    echo "[${id}] cloning ${repo_url}@${repo_rev} → ${cache}"
    rm -rf "$cache"
    git clone --quiet --depth 1 --branch "$repo_rev" "$repo_url" "$cache"
  fi

  arm_objects=()
  for arm in $BENCH_ARMS; do
    tokens_list=""
    turns_list=""
    cost_list=""
    pass_count=0
    for run_idx in $(seq 1 "$BENCH_RUNS"); do
      workdir=".work/run-${id}-${arm}-${run_idx}"
      rm -rf "$workdir"
      cp -R "$cache" "$workdir"
      git -C "$workdir" checkout --quiet -- . 2>/dev/null || true
      git -C "$workdir" clean --quiet -fdx 2>/dev/null || true

      # Prompts go via stdin (a naive map can exceed ARG_MAX as an argument).
      # Prompt and map files live OUTSIDE the working clone so the agent
      # doesn't see harness artifacts in its own file listing.
      prompt_file="$(pwd)/.work/prompt-${id}-${arm}-${run_idx}.txt"
      if [ "$arm" = "with_map" ]; then
        map_file="$(pwd)/.work/map-${id}-${arm}-${run_idx}.md"
        "$REPOMAP_BIN" "$workdir" > "$map_file"
        {
          echo "Repository structure map (generated by repomap — every file's symbol signatures and imports; function bodies omitted):"
          echo
          cat "$map_file"
          echo
          echo "---"
          echo
          printf '%s\n' "$prompt"
        } > "$prompt_file"
      else
        printf '%s\n' "$prompt" > "$prompt_file"
      fi

      echo "[${id}] ${arm} run ${run_idx}/${BENCH_RUNS} …"
      session_json="${workdir}/.session.json"
      # acceptEdits: file edits allowed, bash denied — identical constraints
      # across arms, and the agent can't make network calls or run code.
      (
        cd "$workdir"
        env -u CLAUDECODE -u CLAUDE_CODE_ENTRYPOINT claude -p \
          --output-format json \
          --model "$BENCH_MODEL" \
          --max-turns "$BENCH_MAX_TURNS" \
          --permission-mode acceptEdits \
          < "$prompt_file"
      ) > "$session_json" 2>"${workdir}/.session.err" || true

      # M0 token metric: total input-side tokens processed (fresh + cache
      # creation + cache reads) — the proxy for exploration cost until the
      # tokens-before-first-correct-edit refinement (see README.md).
      tokens="$(jq -r '((.usage.input_tokens // 0) + (.usage.cache_creation_input_tokens // 0) + (.usage.cache_read_input_tokens // 0))' "$session_json" 2>/dev/null || echo null)"
      turns="$(jq -r '.num_turns // null' "$session_json" 2>/dev/null || echo null)"
      cost="$(jq -r '.total_cost_usd // null' "$session_json" 2>/dev/null || echo null)"

      passed=false
      if (cd "$workdir" && bash -c "$success_cmd" >/dev/null 2>&1); then
        passed=true
        pass_count=$((pass_count + 1))
      fi
      echo "[${id}] ${arm} run ${run_idx}: tokens=${tokens} turns=${turns} cost=\$${cost} passed=${passed}"

      [ "$tokens" != "null" ] && tokens_list="${tokens_list}${tokens}"$'\n'
      [ "$turns" != "null" ] && turns_list="${turns_list}${turns}"$'\n'
      [ "$cost" != "null" ] && cost_list="${cost_list}${cost}"$'\n'
    done

    median_tokens="$(printf '%s' "$tokens_list" | median_of)"
    median_turns="$(printf '%s' "$turns_list" | median_of)"
    median_cost="$(printf '%s' "$cost_list" | median_of)"

    # Protocol: >15% token spread across runs makes the comparison suspect —
    # record it instead of hiding it (benchmark/README.md).
    variance_note=null
    if [ "$median_tokens" != "null" ]; then
      tmin="$(printf '%s' "$tokens_list" | grep -v '^$' | sort -n | head -1)"
      tmax="$(printf '%s' "$tokens_list" | grep -v '^$' | sort -n | tail -1)"
      if [ -n "$tmin" ] && [ "$median_tokens" -gt 0 ]; then
        spread_pct=$(((tmax - tmin) * 100 / median_tokens))
        if [ "$spread_pct" -gt 15 ]; then
          variance_note="$(jq -n --arg s "token spread ${spread_pct}% of median across runs (min ${tmin}, max ${tmax}, median ${median_tokens})" '$s')"
        fi
      fi
    fi

    per_run_tokens="$(printf '%s' "$tokens_list" | jq -s '.')"
    per_run_turns="$(printf '%s' "$turns_list" | jq -s '.')"
    arm_objects+=("$(jq -n \
      --arg arm "$arm" \
      --argjson tokens "$median_tokens" \
      --argjson turns "$median_turns" \
      --argjson cost "$median_cost" \
      --argjson runs "$BENCH_RUNS" \
      --argjson passes "$pass_count" \
      --argjson per_tokens "$per_run_tokens" \
      --argjson per_turns "$per_run_turns" \
      --argjson vnote "$variance_note" \
      '{arm: $arm, exploration_tokens: $tokens, turns: $turns, cost_usd: $cost, runs: $runs, passed_runs: $passes, per_run_tokens: $per_tokens, per_run_turns: $per_turns, variance_note: $vnote}')")
  done

  task_objects+=("$(jq -n \
    --arg task "$id" \
    --argjson arms "$(printf '%s\n' "${arm_objects[@]}" | jq -s '.')" \
    '{task: $task, arms: $arms}')")
done

jq -n \
  --argjson schema 2 \
  --arg timestamp "$stamp" \
  --arg model "$BENCH_MODEL" \
  --argjson runs "$BENCH_RUNS" \
  --argjson max_turns "$BENCH_MAX_TURNS" \
  --argjson tasks "$(printf '%s\n' "${task_objects[@]}" | jq -s '.')" \
  '{schema_version: $schema, timestamp: $timestamp, model: $model, runs_per_arm: $runs, max_turns: $max_turns, stub: false, tasks: $tasks}' \
  > "$out"
echo "wrote ${out}"

if [ "$record_baseline" = true ]; then
  # The ONLY sanctioned writer of baseline.json (hand edits are hook-blocked).
  jq -n \
    --slurpfile schema_doc <(jq '._schema' baseline.json) \
    --arg recorded_at "$stamp" \
    --arg environment "$(uname -ms), claude $(claude --version 2>/dev/null | head -1), model ${BENCH_MODEL}, max_turns ${BENCH_MAX_TURNS}" \
    --argjson runs "$BENCH_RUNS" \
    --argjson tasks "$(jq '[.tasks[] | (.arms[] | select(.arm=="without_map")) as $a | {task: .task, exploration_tokens: $a.exploration_tokens, turns: $a.turns, variance_note: $a.variance_note}]' "$out")" \
    '{_schema: $schema_doc[0], schema_version: 1, recorded_at: $recorded_at, environment: $environment, runs_per_arm: $runs, tasks: $tasks}' \
    > baseline.json.tmp && mv baseline.json.tmp baseline.json
  echo "recorded baseline.json (without_map medians, ${BENCH_RUNS} runs/task)"
fi
