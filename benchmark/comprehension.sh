#!/usr/bin/env bash
# repomap comprehension benchmark: does a map in context MAINTAIN (or
# improve) the agent's understanding of the repo, or does it make the agent
# faster but wronger?
#
# Each question runs as one read-only headless session per arm (no edit
# permissions — identical constraints across arms); the answer is scored
# against a pre-verified answer key by substring match. The bar
# (benchmark/README.md): with_map accuracy must be >= without_map accuracy.
#
# Usage (paths relative to benchmark/):
#   ./benchmark/comprehension.sh                                    # default question set
#   ./benchmark/comprehension.sh comprehension/questions-foo.yaml
#
# Env knobs:
#   BENCH_MODEL      model (default: claude-sonnet-4-6)
#   BENCH_MAX_TURNS  per-session turn cap (default: 12)
#   BENCH_ARMS       arms to run (default: "without_map with_map")
#   BENCH_QLIMIT     only the first N questions (smoke runs)
#   ATLAS_BIN        atlas binary for with_map (default: ../target/release/atlas)
#                    (legacy REPOMAP_BIN still honored if set)
#
# Requires: claude CLI (logged in), git, jq, python3 with PyYAML.
set -euo pipefail

cd "$(dirname "$0")"
mkdir -p results .work

BENCH_MODEL="${BENCH_MODEL:-claude-sonnet-4-6}"
BENCH_MAX_TURNS="${BENCH_MAX_TURNS:-12}"
BENCH_ARMS="${BENCH_ARMS:-without_map with_map}"
ATLAS_BIN="${ATLAS_BIN:-${REPOMAP_BIN:-../target/release/atlas}}"
QFILE="${1:-comprehension/questions-pytest-8.2.0.yaml}"

for tool in claude git jq python3; do
  command -v "$tool" >/dev/null 2>&1 || { echo "error: $tool is required" >&2; exit 1; }
done
python3 -c "import yaml" 2>/dev/null || { echo "error: python3 needs PyYAML" >&2; exit 1; }
[ -f "$QFILE" ] && [ -s "$QFILE" ] || { echo "error: no such question file: $QFILE (paths are relative to benchmark/)" >&2; exit 1; }

yq() { # yq <dotted.key.or.index>
  python3 - "$QFILE" "$1" <<'PYEOF'
import sys, yaml
node = yaml.safe_load(open(sys.argv[1]))
for part in sys.argv[2].split('.'):
    node = node[int(part)] if part.isdigit() else node[part]
if isinstance(node, list):
    print('\n'.join(str(x) for x in node))
else:
    print(str(node).strip())
PYEOF
}

median_of() {
  sort -n | awk '{a[NR]=$1} END {if (NR==0) {print "null"; exit}; m=int((NR+1)/2); print a[m]}'
}

repo_url="$(yq repo.url)"
repo_rev="$(yq repo.rev)"
n_questions="$(python3 -c "import yaml,sys; print(len(yaml.safe_load(open('$QFILE'))['questions']))")"
if [ -n "${BENCH_QLIMIT:-}" ] && [ "$BENCH_QLIMIT" -lt "$n_questions" ]; then
  n_questions="$BENCH_QLIMIT"
fi

cache=".work/cache/comprehension-$(basename "$QFILE" .yaml)"
if [ ! -d "$cache/.git" ]; then
  echo "cloning ${repo_url}@${repo_rev} → ${cache}"
  rm -rf "$cache"
  git clone --quiet --depth 1 --branch "$repo_rev" "$repo_url" "$cache"
fi

map_file="$(pwd)/.work/comprehension-map.md"
# COMP_MAP_BUDGET: token budget for the injected map (default 2048 — the shipped
# default). A richer map surfaces more answers directly, so the agent answers
# from context instead of grepping (fewer turns = fewer tokens); tune via env.
case " $BENCH_ARMS " in
  *" with_map "*) "$ATLAS_BIN" "$cache" --budget "${COMP_MAP_BUDGET:-2048}" > "$map_file" ;;
esac

stamp="$(date +%Y%m%d-%H%M%S)"
out="results/comprehension-${stamp}.local.json"
echo "comprehension benchmark — ${n_questions} question(s), arms: ${BENCH_ARMS}, model ${BENCH_MODEL}"

arm_objects=()
for arm in $BENCH_ARMS; do
  correct=0
  tokens_list=""
  turns_list=""
  per_question="[]"
  for i in $(seq 0 $((n_questions - 1))); do
    qid="$(yq "questions.${i}.id")"
    question="$(yq "questions.${i}.question")"
    match_mode="$(yq "questions.${i}.match")"
    expects="$(yq "questions.${i}.expect")"

    prompt_file="$(pwd)/.work/comprehension-prompt-${arm}-${qid}.txt"
    {
      if [ "$arm" = "with_map" ]; then
        echo "Repository structure map (generated by atlas — every file's symbol signatures and imports; function bodies omitted):"
        echo
        cat "$map_file"
        echo
        echo "---"
        echo
      fi
      printf '%s\n\n' "$question"
      echo "Answer concisely; include the exact symbol name and repo-relative file path where applicable."
    } > "$prompt_file"

    session_json="$(pwd)/.work/comprehension-session-${arm}-${qid}.json"
    # No permission flags: in print mode edits are denied, so the session is
    # effectively read-only — identical constraints in both arms.
    (
      cd "$cache"
      env -u CLAUDECODE -u CLAUDE_CODE_ENTRYPOINT claude -p \
        --output-format json \
        --model "$BENCH_MODEL" \
        --max-turns "$BENCH_MAX_TURNS" \
        < "$prompt_file"
    ) > "$session_json" 2>"$(pwd)/.work/comprehension-err-${arm}-${qid}.txt" || true

    answer="$(jq -r '.result // ""' "$session_json" 2>/dev/null || echo "")"
    tokens="$(jq -r '((.usage.input_tokens // 0) + (.usage.cache_creation_input_tokens // 0) + (.usage.cache_read_input_tokens // 0))' "$session_json" 2>/dev/null || echo null)"
    turns="$(jq -r '.num_turns // null' "$session_json" 2>/dev/null || echo null)"

    hits=0
    misses=0
    while IFS= read -r expected; do
      [ -n "$expected" ] || continue
      if [ "${answer#*"$expected"}" != "$answer" ]; then
        hits=$((hits + 1))
      else
        misses=$((misses + 1))
      fi
    done <<< "$expects"
    passed=false
    if [ "$match_mode" = "any" ] && [ "$hits" -gt 0 ]; then passed=true; fi
    if [ "$match_mode" = "all" ] && [ "$misses" -eq 0 ] && [ "$hits" -gt 0 ]; then passed=true; fi
    [ "$passed" = true ] && correct=$((correct + 1))

    echo "[${arm}] ${qid}: correct=${passed} tokens=${tokens} turns=${turns}"
    [ "$tokens" != "null" ] && tokens_list="${tokens_list}${tokens}"$'\n'
    [ "$turns" != "null" ] && turns_list="${turns_list}${turns}"$'\n'
    per_question="$(jq -n --argjson prev "$per_question" --arg id "$qid" --argjson ok "$passed" '$prev + [{id: $id, correct: $ok}]')"
  done

  accuracy="$(python3 -c "print(round(${correct}/${n_questions}, 3))")"
  arm_objects+=("$(jq -n \
    --arg arm "$arm" \
    --argjson accuracy "$accuracy" \
    --argjson correct "$correct" \
    --argjson n "$n_questions" \
    --argjson tokens "$(printf '%s' "$tokens_list" | median_of)" \
    --argjson turns "$(printf '%s' "$turns_list" | median_of)" \
    --argjson perq "$per_question" \
    '{arm: $arm, accuracy: $accuracy, correct: $correct, n: $n, median_tokens: $tokens, median_turns: $turns, per_question: $perq}')")
  echo "[${arm}] accuracy: ${correct}/${n_questions} (${accuracy})"
done

jq -n \
  --arg timestamp "$stamp" \
  --arg model "$BENCH_MODEL" \
  --arg qfile "$QFILE" \
  --argjson max_turns "$BENCH_MAX_TURNS" \
  --argjson arms "$(printf '%s\n' "${arm_objects[@]}" | jq -s '.')" \
  '{schema_version: 1, type: "comprehension", timestamp: $timestamp, model: $model, question_file: $qfile, max_turns: $max_turns, arms: $arms}' \
  > "$out"
echo "wrote ${out}"
